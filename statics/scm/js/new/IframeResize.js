
function Resize(id) {

    if (document.getElementById("dbody")) {
        if (parent.document.getElementById(id)) {
            parent.document.getElementById(id).height = document.getElementById("dbody").offsetHeight + 60;
        }
    }

    if (document.getElementById("dbody")) {
        if (parent.document.getElementById(id)) {
            parent.document.getElementById(id).height = document.getElementById("dbody").offsetHeight + 60;
        }
    }

}



